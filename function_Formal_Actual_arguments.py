## Formal and Actual Arguments

## Actual arguemnts : 4 types:
# Positional arguments
# Keyword arguments
# Default arguments
# Variable length arguments 



def sum(a, b):      # a, b are formal arguments --> parameters/variable names mentioned while defining a function 
    c = a + b 
    print(c)
    
    
# call the function 
x = 10; y = 15
sum(x, y)           # x, y are actual arguments --> values passed while calling a function 


