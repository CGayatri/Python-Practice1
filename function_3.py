## Program 3 - to test whether a number is even or odd

def even_odd(num):
    """ to knwo num is even or odd """
    if (num%2 == 0):
        print(num, ' is even')
    else :
        print(num, ' is odd')
    
# call the function 
even_odd(12)
even_odd(13)


