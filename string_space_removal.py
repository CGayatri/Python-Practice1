## Removing spaces from a string - strip(), rstrip(), lstrip()

name = ' Mukund Deshmukh  '

print(name.rstrip())
# Mukund Deshmukh

print(name.lstrip())
'''
F:\PY>py string_space_removal.py
 Mukund Deshmukh
Mukund Deshmukh
'''

print(name.strip())
'''
F:\PY>py string_space_removal.py
 Mukund Deshmukh
Mukund Deshmukh
Mukund Deshmukh
'''
